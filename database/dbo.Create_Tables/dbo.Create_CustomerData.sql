CREATE TABLE dbo.CustomerData(
        Email_ID VARCHAR(55) NOT NULL,
        State VARCHAR(55) NOT NULL,
        City VARCHAR(55) NOT NULL,
        Vehicle_Type VARCHAR(55),
        Commute_Miles_MM INT,
        Commute_Time_MM INT,
        Commute_Miles_YYYY INT,
        Commute_Time_YYYY INT,
        CO2_Emission_MM INT,
        CO2_Emission_YYYY INT
);

